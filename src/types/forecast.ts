interface ForecastUnits {
  time: string;
  interval: string;
  temperature: string;
  windspeed: string;
  winddirection: string;
  is_day: string;
  weathercode: string;
}

interface ForecastWeather {
  time: string;
  interval: number;
  temperature: number;
  windspeed: number;
  winddirection: number;
  is_day: number;
  weathercode: number;
}

export interface Forecast {
  latitude: number;
  longitude: number;
  generationtime_ms: number;
  utc_offset_seconds: number;
  timezone: string;
  timezone_abbreviation: string;
  elevation: number;
  current_weather_units: ForecastUnits;
  current_weather: ForecastWeather;
}

export interface WeatherStorage {
  forecast: Forecast;
  timestamp: number;
  coords: {
    lat: number;
    lng: number;
  };
}
